def con_date(d):
    if d is 'A':
        return '200'
    elif d is 'B':
        return '201'
    elif d is 'C':
        return '202'
    elif d is 'D':
        return '203'
    elif d is 'E':
        return '204'
    elif d is 'F':
        return '205'
    elif d is 'G':
        return '206'
    elif d is 'H':
        return '207'
    elif d is 'I':
        return '208'
    elif d is 'J':
        return '209'
    else:
        return ''
